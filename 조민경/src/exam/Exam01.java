package exam;

public class Exam01 {
	public static void main(String[] args) {
		//문제 1.
		int x = 10;
		double y = 2.0;
		int sum = x+(int)y;
		int minu = x-(int)y;
		int gop = x/(int)y;
		int na = x*(int)y;
		
		System.out.println("덧셈 결과 : "+sum);
		System.out.println("뺄셈 결과 : "+minu);
		System.out.println("곱셈 결과 : "+gop);
		System.out.println("나눗셈 결과 : "+na);
		
		
		
	}
}
